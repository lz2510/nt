<?php

/**
 * 
 */
class Meal
{
	public $food;
	public $drink;
	
	
	
	public function getFood()
	{
		return $this->food;
	}
	
	public function setFood($food)
	{
		$this->food = $food;
	}
	
	public function getDrink()
	{
		return $this->drink;
	}
	
	public function setDrink($drink)
	{
		$this->drink = $drink;
	}
}



